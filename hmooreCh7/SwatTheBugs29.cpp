#include <iostream>
using namespace std;

int main()
{
	// declare and initialize number
	int number = 1;

	// while the number is less than 5
	while (number < 5)
	{
		// display the number
		cout << number << endl;
		// number increases by 1
		number += 1;
	} //end while
	return 0;
}	//end of main function
